<h2>How Does a Merchant Cash Advance (MCA) Work?</h2>

<p>The fundamental mechanism of a Merchant Cash Advance is identical for businesses in both the United States and Canada. It is a simple concept: a funding company provides you with a lump sum of cash upfront in exchange for purchasing a percentage of your future sales at a discount.</p>

<p>Here''s a step-by-step breakdown of the process:</p>

<h3>1. You Receive a Cash Advance</h3>

<p>Based on your business''s average monthly sales volume, an MCA provider will offer you an upfront sum of cash. For example, let''s say you are advanced $20,000.</p>

<h3>2. You Agree on a "Factor Rate"</h3>

<p>Instead of a traditional interest rate (APR), MCAs use a factor rate. This is a simple multiplier that determines the total amount you will repay. Factor rates typically range from 1.1 to 1.5, depending on the provider''s assessment of your business''s risk.</p>

<p>If you agree to a factor rate of 1.3, the total repayment amount would be:<br>
$20,000 (Advance) x 1.3 (Factor Rate) = $26,000 (Total Repayment)</p>

